#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
B2/B3 evaluation script (locked degrees of freedom)

Input:
  - merged long-format CSV with clinical label column `response_label` (0/1)
    generated by your merge script.

Locked main-analysis choices:
  - unit = mutation
  - binder_metric = rank
  - strategy = R1

Pre-registered metrics (4):
  1) baseline: count | all
  2) B2:      count | vaf>=0.3
  3) B3:      weighted | all           (RNA subset only)
  4) B2+B3:   weighted | vaf>=0.3      (RNA subset only)

Outputs:
  - summary_full.csv (n=33 cohort)
  - summary_rna.csv  (n=24 cohort defined as having weighted rows)
  - Figures (PNG):
      Fig1_ORR_distribution_full.png
      Fig2_ROC_full_baseline_vs_B2.png
      Fig3_ROC_RNA_count_vs_weighted.png
      FigS1_VAF_scan_full_count.png
      FigS2_VAF_scan_RNA_count_vs_weighted.png
"""

import argparse
import os
import math
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

from sklearn.metrics import roc_auc_score, roc_curve
from sklearn.preprocessing import StandardScaler
from sklearn.linear_model import LogisticRegression


# ----------------------------
# Helpers
# ----------------------------
def build_key(df: pd.DataFrame) -> pd.Series:
    return (
        df["unit"].astype(str) + "|" +
        df["binder_metric"].astype(str) + "|" +
        df["strategy"].astype(str) + "|" +
        df["metric_family"].astype(str) + "|" +
        df["clonal_cutoff"].astype(str)
    )


def ensure_dir(path: str):
    os.makedirs(path, exist_ok=True)


def _finite_xy(y, x):
    x = np.asarray(x, dtype=float)
    y = np.asarray(y, dtype=int)
    ok = np.isfinite(x) & np.isfinite(y)
    return y[ok], x[ok]


def auc_bootstrap_ci(y, x, n_boot=2000, seed=1):
    """
    Bootstrap AUC (apparent AUC on bootstrap sample) + CI.
    """
    y, x = _finite_xy(y, x)
    if len(x) < 6 or len(np.unique(y)) < 2:
        return np.nan, np.nan, np.nan, 0

    rng = np.random.default_rng(seed)
    n = len(y)
    auc0 = roc_auc_score(y, x)

    aucs = []
    kept = 0
    for _ in range(n_boot):
        idx = rng.integers(0, n, size=n)
        yb, xb = y[idx], x[idx]
        if len(np.unique(yb)) < 2:
            continue
        kept += 1
        aucs.append(roc_auc_score(yb, xb))

    if kept == 0:
        return auc0, np.nan, np.nan, 0

    lo, hi = np.quantile(np.asarray(aucs), [0.025, 0.975])
    return auc0, float(lo), float(hi), kept


def paired_boot_delta_auc(y, x0, x1, n_boot=2000, seed=1):
    """
    Paired bootstrap delta AUC: sample indices once, compute both AUCs on same resample.
    Returns delta_mean, CI, P(delta>0), kept.
    """
    y, x0 = _finite_xy(y, x0)
    _, x1 = _finite_xy(y, x1)  # aligns length if x1 had NaNs at same places (we pre-align samples anyway)

    if len(x0) < 6 or len(np.unique(y)) < 2:
        return np.nan, np.nan, np.nan, np.nan, 0

    rng = np.random.default_rng(seed)
    n = len(y)
    deltas = []
    kept = 0
    for _ in range(n_boot):
        idx = rng.integers(0, n, size=n)
        yb = y[idx]
        if len(np.unique(yb)) < 2:
            continue
        d = roc_auc_score(yb, x1[idx]) - roc_auc_score(yb, x0[idx])
        deltas.append(d)
        kept += 1

    if kept == 0:
        return np.nan, np.nan, np.nan, np.nan, 0

    deltas = np.asarray(deltas, dtype=float)
    lo, hi = np.quantile(deltas, [0.025, 0.975])
    p_pos = float((deltas > 0).mean())
    return float(deltas.mean()), float(lo), float(hi), p_pos, kept


def fit_logistic_or_ci(y, x, n_boot=2000, seed=1):
    """
    Fit single-predictor logistic regression on z-scored x:
      response ~ z(x)
    Return OR per 1 SD, 95% bootstrap CI, and fitted probabilities for ROC plotting.
    """
    y, x = _finite_xy(y, x)
    if len(x) < 6 or len(np.unique(y)) < 2:
        return np.nan, np.nan, np.nan, None

    # Fit on full data
    X = StandardScaler().fit_transform(x.reshape(-1, 1))
    lr = LogisticRegression(solver="liblinear")
    lr.fit(X, y)
    coef = float(lr.coef_[0, 0])
    or_full = float(math.exp(coef))
    prob = lr.predict_proba(X)[:, 1]

    # Bootstrap OR
    rng = np.random.default_rng(seed)
    n = len(y)
    ors = []
    kept = 0
    for _ in range(n_boot):
        idx = rng.integers(0, n, size=n)
        yb, xb = y[idx], x[idx]
        if len(np.unique(yb)) < 2:
            continue
        Xb = StandardScaler().fit_transform(xb.reshape(-1, 1))
        lrb = LogisticRegression(solver="liblinear")
        lrb.fit(Xb, yb)
        cb = float(lrb.coef_[0, 0])
        ors.append(math.exp(cb))
        kept += 1

    if kept == 0:
        return or_full, np.nan, np.nan, prob

    lo, hi = np.quantile(np.asarray(ors, dtype=float), [0.025, 0.975])
    return or_full, float(lo), float(hi), prob


def get_patient_series(df, key, cohort_samples=None):
    """
    Return patient-aligned dataframe: sample, response_label, value
    """
    g = df[df["key"] == key][["sample", "response_label", "value"]].drop_duplicates(subset=["sample"]).copy()
    g = g.dropna(subset=["response_label", "value"])
    if cohort_samples is not None:
        g = g[g["sample"].isin(cohort_samples)].copy()
    g["response_label"] = g["response_label"].astype(int)
    g["value"] = g["value"].astype(float)
    return g


def plot_orr_distribution(g_base, g_b2, out_png, title_left, title_right):
    """
    Two-panel scatter+box-like visualization (simple, readable for n small).
    """
    # align samples
    common = sorted(set(g_base["sample"]) & set(g_b2["sample"]))
    gb = g_base.set_index("sample").loc[common].reset_index()
    g2 = g_b2.set_index("sample").loc[common].reset_index()

    def _panel(ax, g, title):
        y = g["response_label"].to_numpy()
        x = g["value"].to_numpy()

        # jittered scatter
        rng = np.random.default_rng(1)
        jitter0 = rng.normal(0, 0.04, size=(y == 0).sum())
        jitter1 = rng.normal(0, 0.04, size=(y == 1).sum())
        ax.scatter(np.zeros((y == 0).sum()) + jitter0, x[y == 0], alpha=0.8)
        ax.scatter(np.ones((y == 1).sum()) + jitter1, x[y == 1], alpha=0.8)

        # simple median lines
        if (y == 0).sum() > 0:
            ax.hlines(np.median(x[y == 0]), -0.25, 0.25)
        if (y == 1).sum() > 0:
            ax.hlines(np.median(x[y == 1]), 0.75, 1.25)

        ax.set_xticks([0, 1])
        ax.set_xticklabels(["Non-resp", "Resp"])
        ax.set_title(title)
        ax.set_xlim(-0.5, 1.5)
        ax.set_ylabel("TNB score (value)")

    fig = plt.figure(figsize=(10, 4))
    ax1 = fig.add_subplot(1, 2, 1)
    ax2 = fig.add_subplot(1, 2, 2)

    _panel(ax1, gb, title_left)
    _panel(ax2, g2, title_right)

    fig.suptitle("ORR distribution (locked: unit=mutation, binder=rank, strategy=R1)")
    fig.tight_layout()
    fig.savefig(out_png, dpi=200)
    plt.close(fig)


def plot_roc_two(y, prob_a, prob_b, label_a, label_b, out_png, title):
    fpr_a, tpr_a, _ = roc_curve(y, prob_a)
    fpr_b, tpr_b, _ = roc_curve(y, prob_b)

    auc_a = roc_auc_score(y, prob_a) if len(np.unique(y)) == 2 else np.nan
    auc_b = roc_auc_score(y, prob_b) if len(np.unique(y)) == 2 else np.nan

    fig = plt.figure(figsize=(5, 5))
    ax = fig.add_subplot(1, 1, 1)
    ax.plot(fpr_a, tpr_a, label=f"{label_a} (AUC={auc_a:.3f})")
    ax.plot(fpr_b, tpr_b, label=f"{label_b} (AUC={auc_b:.3f})")
    ax.plot([0, 1], [0, 1], linestyle="--")
    ax.set_xlabel("False Positive Rate")
    ax.set_ylabel("True Positive Rate")
    ax.set_title(title)
    ax.legend(loc="lower right")
    fig.tight_layout()
    fig.savefig(out_png, dpi=200)
    plt.close(fig)


def plot_vaf_scan(scan_df, out_png, title):
    """
    scan_df columns: clonal_cutoff, auc, ci_low, ci_high, series_name
    """
    # order cutoffs: all then numeric
    def _order(c):
        if c == "all":
            return -1
        try:
            return float(c.replace("vaf>=", ""))
        except Exception:
            return 999

    scan_df = scan_df.copy()
    scan_df["x_ord"] = scan_df["clonal_cutoff"].astype(str).map(_order)
    scan_df = scan_df.sort_values(["series_name", "x_ord"])

    fig = plt.figure(figsize=(8, 4.5))
    ax = fig.add_subplot(1, 1, 1)

    for name, g in scan_df.groupby("series_name"):
        xs = g["x_ord"].to_numpy()
        ys = g["auc"].to_numpy()
        ylo = g["ci_low"].to_numpy()
        yhi = g["ci_high"].to_numpy()
        ax.plot(xs, ys, marker="o", label=name)
        ax.vlines(xs, ylo, yhi, alpha=0.6)

    # x tick labels
    uniq = scan_df.drop_duplicates("x_ord")[["x_ord", "clonal_cutoff"]].sort_values("x_ord")
    ax.set_xticks(uniq["x_ord"].to_list())
    ax.set_xticklabels(uniq["clonal_cutoff"].to_list(), rotation=0)

    ax.set_ylim(0.0, 1.0)
    ax.set_ylabel("AUC (bootstrap CI)")
    ax.set_title(title)
    ax.legend(loc="lower right")
    fig.tight_layout()
    fig.savefig(out_png, dpi=200)
    plt.close(fig)


# ----------------------------
# Main
# ----------------------------
def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--merged-csv", required=True, help="Merged long-format CSV with clinical labels")
    ap.add_argument("--outdir", required=True, help="Output directory")
    ap.add_argument("--n-boot", type=int, default=2000, help="Bootstrap replicates")
    ap.add_argument("--seed", type=int, default=1, help="Random seed")
    ap.add_argument("--vaf-cutoffs", default="0.2,0.25,0.3,0.35", help="VAF cutoffs for scan")
    args = ap.parse_args()

    ensure_dir(args.outdir)

    df = pd.read_csv(args.merged_csv)
    required = {"sample", "unit", "binder_metric", "strategy", "metric_family", "clonal_cutoff", "value", "response_label"}
    miss = required - set(df.columns)
    if miss:
        raise ValueError(f"Missing required columns in merged CSV: {sorted(miss)}")

    # normalize
    df["sample"] = df["sample"].astype(str).str.strip()
    df["response_label"] = pd.to_numeric(df["response_label"], errors="coerce")
    df = df.dropna(subset=["response_label"]).copy()
    df["response_label"] = df["response_label"].astype(int)

    df["key"] = build_key(df)
    df = df.drop_duplicates(subset=["sample", "key"], keep="first").copy()

    # lock main axis
    locked = df[
        (df["unit"].astype(str) == "mutation") &
        (df["binder_metric"].astype(str) == "rank") &
        (df["strategy"].astype(str) == "R1")
    ].copy()

    if locked.empty:
        raise ValueError("No rows found for locked setting: unit=mutation, binder_metric=rank, strategy=R1")

    # Define keys (pre-registered)
    key_baseline = "mutation|rank|R1|count|all"
    key_b2 = "mutation|rank|R1|count|vaf>=0.3"
    key_w_all = "mutation|rank|R1|weighted|all"
    key_w_b2 = "mutation|rank|R1|weighted|vaf>=0.3"

    # Full cohort samples: all with baseline available
    g_base_full = get_patient_series(locked, key_baseline, cohort_samples=None)
    full_samples = sorted(g_base_full["sample"].unique())

    # RNA cohort definition (locked, no extra freedom):
    # samples that have weighted rows under the same locked axis (unit=mutation,binder=rank,strategy=R1)
    g_w_all_any = get_patient_series(locked, key_w_all, cohort_samples=None)
    rna_samples = sorted(g_w_all_any["sample"].unique())

    # ------------------------
    # FULL cohort: baseline vs B2
    # ------------------------
    g_base = get_patient_series(locked, key_baseline, cohort_samples=full_samples)
    g_b2 = get_patient_series(locked, key_b2, cohort_samples=full_samples)

    common_full = sorted(set(g_base["sample"]) & set(g_b2["sample"]))
    g_base = g_base.set_index("sample").loc[common_full].reset_index()
    g_b2 = g_b2.set_index("sample").loc[common_full].reset_index()

    y_full = g_base["response_label"].to_numpy()
    x_base = g_base["value"].to_numpy()
    x_b2 = g_b2["value"].to_numpy()

    # AUC + CI
    auc_base, lo_base, hi_base, kept_base = auc_bootstrap_ci(y_full, x_base, n_boot=args.n_boot, seed=args.seed)
    auc_b2, lo_b2, hi_b2, kept_b2 = auc_bootstrap_ci(y_full, x_b2, n_boot=args.n_boot, seed=args.seed)

    # Paired delta
    dmean, dlo, dhi, ppos, kept_d = paired_boot_delta_auc(y_full, x_base, x_b2, n_boot=args.n_boot, seed=args.seed)

    # OR + CI + probs for ROC
    or_base, orlo_base, orhi_base, prob_base = fit_logistic_or_ci(y_full, x_base, n_boot=args.n_boot, seed=args.seed)
    or_b2, orlo_b2, orhi_b2, prob_b2 = fit_logistic_or_ci(y_full, x_b2, n_boot=args.n_boot, seed=args.seed)

    summary_full = pd.DataFrame([
        {
            "cohort": "full",
            "key": key_baseline,
            "n": int(len(common_full)),
            "responders": int(y_full.sum()),
            "auc": auc_base, "ci_low": lo_base, "ci_high": hi_base, "boot_kept": kept_base,
            "OR_per_1SD": or_base, "OR_ci_low": orlo_base, "OR_ci_high": orhi_base
        },
        {
            "cohort": "full",
            "key": key_b2,
            "n": int(len(common_full)),
            "responders": int(y_full.sum()),
            "auc": auc_b2, "ci_low": lo_b2, "ci_high": hi_b2, "boot_kept": kept_b2,
            "OR_per_1SD": or_b2, "OR_ci_low": orlo_b2, "OR_ci_high": orhi_b2
        },
        {
            "cohort": "full",
            "key": "DELTA_AUC(B2-baseline)",
            "n": int(len(common_full)),
            "responders": int(y_full.sum()),
            "auc": dmean, "ci_low": dlo, "ci_high": dhi, "boot_kept": kept_d,
            "OR_per_1SD": np.nan, "OR_ci_low": np.nan, "OR_ci_high": np.nan,
            "P(delta_auc>0)": ppos
        }
    ])

    summary_full_path = os.path.join(args.outdir, "summary_full.csv")
    summary_full.to_csv(summary_full_path, index=False)

    # Figures (full)
    fig1 = os.path.join(args.outdir, "Fig1_ORR_distribution_full.png")
    plot_orr_distribution(
        g_base, g_b2, fig1,
        title_left="Baseline (count | all)",
        title_right="B2 clonal (count | vaf>=0.3)"
    )

    fig2 = os.path.join(args.outdir, "Fig2_ROC_full_baseline_vs_B2.png")
    # If prob is None (rare), fallback to raw x as score
    score_a = prob_base if prob_base is not None else x_base
    score_b = prob_b2 if prob_b2 is not None else x_b2
    plot_roc_two(
        y_full, score_a, score_b,
        label_a="Baseline", label_b="B2 clonal",
        out_png=fig2,
        title="ROC (full cohort, locked axis)"
    )

    # Supplementary scan: full cohort count across VAF cutoffs
    vaf_list = [s.strip() for s in args.vaf_cutoffs.split(",") if s.strip()]
    scan_rows = []
    for v in vaf_list:
        key = f"mutation|rank|R1|count|vaf>={v}"
        g = get_patient_series(locked, key, cohort_samples=full_samples)
        common = sorted(set(g_base_full["sample"]) & set(g["sample"]))
        if len(common) < 6:
            continue
        gg = g.set_index("sample").loc[common]
        yy = gg["response_label"].to_numpy()
        xx = gg["value"].to_numpy()
        auc, lo, hi, kept = auc_bootstrap_ci(yy, xx, n_boot=args.n_boot, seed=args.seed)
        scan_rows.append({"series_name": "count", "clonal_cutoff": f"vaf>={v}", "auc": auc, "ci_low": lo, "ci_high": hi, "boot_kept": kept})

    # include all baseline point
    scan_rows.append({"series_name": "count", "clonal_cutoff": "all", "auc": auc_base, "ci_low": lo_base, "ci_high": hi_base, "boot_kept": kept_base})

    scan_full = pd.DataFrame(scan_rows)
    figS1 = os.path.join(args.outdir, "FigS1_VAF_scan_full_count.png")
    plot_vaf_scan(scan_full, figS1, title="VAF cutoff scan (full cohort, count; locked axis)")

    scan_full_path = os.path.join(args.outdir, "scan_full_count_vaf.csv")
    scan_full.to_csv(scan_full_path, index=False)

    # ------------------------
    # RNA cohort: count vs weighted (and B2+B3)
    # ------------------------
    # Build RNA-aligned datasets
    g_count_all = get_patient_series(locked, key_baseline, cohort_samples=rna_samples)
    g_count_b2 = get_patient_series(locked, key_b2, cohort_samples=rna_samples)
    g_w_all = get_patient_series(locked, key_w_all, cohort_samples=rna_samples)
    g_w_b2 = get_patient_series(locked, key_w_b2, cohort_samples=rna_samples)

    # For fair comparisons, always align on intersection of the two metrics being compared.
    # Compare (count vaf>=0.3) vs (weighted vaf>=0.3) as the main RNA ROC (B2+B3 question)
    common_rna = sorted(set(g_count_b2["sample"]) & set(g_w_b2["sample"]))
    if len(common_rna) >= 6:
        gc = g_count_b2.set_index("sample").loc[common_rna].reset_index()
        gw = g_w_b2.set_index("sample").loc[common_rna].reset_index()

        y_rna = gc["response_label"].to_numpy()
        x_count = gc["value"].to_numpy()
        x_w = gw["value"].to_numpy()

        auc_count, lo_count, hi_count, kept_count = auc_bootstrap_ci(y_rna, x_count, n_boot=args.n_boot, seed=args.seed)
        auc_w, lo_w, hi_w, kept_w = auc_bootstrap_ci(y_rna, x_w, n_boot=args.n_boot, seed=args.seed)
        dmean2, dlo2, dhi2, ppos2, kept_d2 = paired_boot_delta_auc(y_rna, x_count, x_w, n_boot=args.n_boot, seed=args.seed)

        or_count, orlo_count, orhi_count, prob_count = fit_logistic_or_ci(y_rna, x_count, n_boot=args.n_boot, seed=args.seed)
        or_w, orlo_w, orhi_w, prob_w = fit_logistic_or_ci(y_rna, x_w, n_boot=args.n_boot, seed=args.seed)

        summary_rna = pd.DataFrame([
            {
                "cohort": "rna",
                "key": key_b2,
                "n": int(len(common_rna)),
                "responders": int(y_rna.sum()),
                "auc": auc_count, "ci_low": lo_count, "ci_high": hi_count, "boot_kept": kept_count,
                "OR_per_1SD": or_count, "OR_ci_low": orlo_count, "OR_ci_high": orhi_count
            },
            {
                "cohort": "rna",
                "key": key_w_b2,
                "n": int(len(common_rna)),
                "responders": int(y_rna.sum()),
                "auc": auc_w, "ci_low": lo_w, "ci_high": hi_w, "boot_kept": kept_w,
                "OR_per_1SD": or_w, "OR_ci_low": orlo_w, "OR_ci_high": orhi_w
            },
            {
                "cohort": "rna",
                "key": "DELTA_AUC(weighted-count) @ vaf>=0.3",
                "n": int(len(common_rna)),
                "responders": int(y_rna.sum()),
                "auc": dmean2, "ci_low": dlo2, "ci_high": dhi2, "boot_kept": kept_d2,
                "OR_per_1SD": np.nan, "OR_ci_low": np.nan, "OR_ci_high": np.nan,
                "P(delta_auc>0)": ppos2
            }
        ])

        summary_rna_path = os.path.join(args.outdir, "summary_rna.csv")
        summary_rna.to_csv(summary_rna_path, index=False)

        # RNA ROC figure
        fig3 = os.path.join(args.outdir, "Fig3_ROC_RNA_count_vs_weighted.png")
        score_c = prob_count if prob_count is not None else x_count
        score_w = prob_w if prob_w is not None else x_w
        plot_roc_two(
            y_rna, score_c, score_w,
            label_a="Count (vaf>=0.3)", label_b="Weighted (vaf>=0.3)",
            out_png=fig3,
            title="ROC (RNA subset, locked axis)"
        )

        # Supplementary scan (RNA): count vs weighted across VAF cutoffs
        scan_rows2 = []
        # baseline points at all
        # For "all", align count_all with w_all
        common_all = sorted(set(g_count_all["sample"]) & set(g_w_all["sample"]))
        if len(common_all) >= 6:
            gca = g_count_all.set_index("sample").loc[common_all]
            gwa = g_w_all.set_index("sample").loc[common_all]
            yy = gca["response_label"].to_numpy()
            auc_c, lo_c, hi_c, kept_c = auc_bootstrap_ci(yy, gca["value"].to_numpy(), n_boot=args.n_boot, seed=args.seed)
            auc_w0, lo_w0, hi_w0, kept_w0 = auc_bootstrap_ci(yy, gwa["value"].to_numpy(), n_boot=args.n_boot, seed=args.seed)
            scan_rows2.append({"series_name": "count", "clonal_cutoff": "all", "auc": auc_c, "ci_low": lo_c, "ci_high": hi_c, "boot_kept": kept_c})
            scan_rows2.append({"series_name": "weighted", "clonal_cutoff": "all", "auc": auc_w0, "ci_low": lo_w0, "ci_high": hi_w0, "boot_kept": kept_w0})

        for v in vaf_list:
            kc = f"mutation|rank|R1|count|vaf>={v}"
            kw = f"mutation|rank|R1|weighted|vaf>={v}"
            gc = get_patient_series(locked, kc, cohort_samples=rna_samples)
            gw = get_patient_series(locked, kw, cohort_samples=rna_samples)
            common = sorted(set(gc["sample"]) & set(gw["sample"]))
            if len(common) < 6:
                continue
            gc2 = gc.set_index("sample").loc[common]
